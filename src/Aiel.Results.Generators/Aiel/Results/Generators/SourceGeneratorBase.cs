using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Aiel.Roslyn;
using System.Collections.Immutable;
using System.Reflection;

namespace Aiel.Results.Generators;

public abstract class SourceGeneratorBase
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // STEP 1: Find all class declarations with "partial" and ": Error"
        var errorClasses = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => IsCandidate(node),
                transform: static (ctx, _) => GetErrorSymbol(ctx))
            .Where(static m => m is not null);

        // STEP 2: Collect them into an array
        var collected = errorClasses.Collect();

        // STEP 3: Generate code
        context.RegisterSourceOutput(collected, (spc, errors)
            => Generate(spc, errors!));
    }

    protected abstract void Generate(SourceProductionContext spc, ImmutableArray<INamedTypeSymbol> namedTypeSymbols);

    private static DateTime BuildDate
         => new DateTime(2000, 1, 1).AddDays(GeneratorVersion.Build).AddSeconds(GeneratorVersion.Revision * 2);
    protected static String DisplayVersion => $"{GeneratorVersion} ({BuildDate})";
    protected static String GeneratorName => nameof(ErrorClassGenerator);

    protected static Version GeneratorVersion
         => Assembly.GetExecutingAssembly().GetName().Version;

    protected static String Header(String passName)
    {
        var assemblyName = typeof(ErrorClassGenerator).Assembly.GetName().Name;
        var localTime = DateTime.UtcNow.ToLocalTime().ToString("o");

        return
            $"""
        // <auto-generated>
        //   This file was brought to you by {assemblyName}
        //   Version: {DisplayVersion}
        //   Generator: {GeneratorName}
        //   Pass: {passName}
        //   Generated at: {localTime} PST {DateTime.Now.Ticks}
        //
        //   DO NOT EDIT THIS FILE BY HAND OR THE WORLD MAY END!
        //   (Seriously. The generator will overwrite your changes anyway.)
        //
        // </auto-generated>

        """;
    }

    private static INamedTypeSymbol? GetErrorSymbol(GeneratorSyntaxContext ctx)
    {
        var cds = (ClassDeclarationSyntax)ctx.Node;

        if (ctx.SemanticModel.GetDeclaredSymbol(cds) is not INamedTypeSymbol symbol)
        {
            return null;
        }

        // Must derive from Error
        var baseType = symbol.BaseType;

        while (baseType is not null)
        {
            var fullName = baseType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
            if (fullName == GeneratorConsts.FqErrorType)
            {
                return symbol;
            }

            baseType = baseType.BaseType;
        }

        return null;
    }

    private static Boolean IsCandidate(SyntaxNode node)
    {
        return node is ClassDeclarationSyntax cds
            && cds.Modifiers.Any(m => m.IsKind(SyntaxKind.PartialKeyword));
    }

    protected static String LowerFirst(String name)
        => String.IsNullOrEmpty(name) ? name : Char.ToLowerInvariant(name[0]) + name.Substring(1);

    protected static String UpperFirst(String name)
        => String.IsNullOrEmpty(name) ? name : Char.ToUpperInvariant(name[0]) + name.Substring(1);
}
