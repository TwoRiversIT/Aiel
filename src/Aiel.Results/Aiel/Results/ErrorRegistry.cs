using Aiel.Roslyn;
using System.Collections.Concurrent;
using System.Reflection;

namespace Aiel.Results;

/// <summary>
/// Serves as a registry for all <see cref="Error"/> types and their corresponding error code types.
/// </summary>
/// <remarks>
/// <para>
/// Error types are automatically registered via source-generated static initializer. Each assembly
/// containing public partial classes derived from <see cref="Error"/> will have its errors
/// registered when the assembly is loaded.
/// </para>
/// <para>
/// This means that in most cases, you should not need to manually call the Register method for your
/// custom error types, as they will be registered automatically when the assembly is loaded. The
/// exception is if you are using custom errors that are not generated by the source generator, in
/// which case you MAY need to call Register for those types to ensure they can be  are properly registered in the lookup table.
/// </para>
/// </remarks>
public static class ErrorRegistry
{
#if NET9_0_OR_GREATER
    private static readonly Lock LockObject = new();
#else
    private static readonly Object LockObject = new();
#endif

    internal static ConcurrentDictionary<Type, ErrorCode> LookupByType { get; } = new();
    internal static ConcurrentDictionary<String, Type> ErrorTypesByName { get; } = new();
    internal static ConcurrentDictionary<String, Type> ErrorTypeByDiscriminator { get; } = new();
    internal static ConcurrentDictionary<Type, String> DiscriminatorByErrorType { get; } = new();

    /// <summary>
    /// Registers the specified error ErrorType for use within the application.
    /// </summary>
    /// <remarks>This method initializes the error handling mechanism for the specified error ErrorType, allowing
    /// it to be utilized throughout the application. It is important to ensure that the ErrorType parameter TError is a
    /// valid subclass of Error to avoid runtime exceptions.</remarks>
    /// <typeparam name="TError">The ErrorType of error to register. Must derive from the base class Error.</typeparam>
    public static void Register<TError>() where TError : Error
    {
        lock (LockObject)
        {
            var errorType = typeof(TError);
            var code = GetErrorCodeFor(errorType);
            // Your discriminator can be whatever you want—this is stable and versionable.
            var discriminator = $"{code.GetType().FullName}:v1";
            //ErrorTypesByName[errorType.FullName!] = errorType;
            _ = ErrorTypesByName.GetOrAdd(errorType.FullName!, errorType);

            ErrorTypeByDiscriminator[discriminator] = errorType;
            DiscriminatorByErrorType[errorType] = discriminator;
            LookupByType[errorType] = CreateInstance(errorType);
        }
    }

    /// <summary>
    /// Retrieves the error type associated with the specified fully qualified name.
    /// </summary>
    /// <remarks>This method searches a predefined collection of error types. Ensure that the provided name is
    /// valid to avoid exceptions.</remarks>
    /// <param name="fullName">The fully qualified name of the error type to retrieve. This parameter cannot be null or empty.</param>
    /// <returns>The <see cref="Type"/> that corresponds to the specified error type name.</returns>
    /// <exception cref="ArgumentException">Thrown if <paramref name="fullName"/> does not correspond to any known error type.</exception>
    public static Type GetErrorType(String fullName)
        => ErrorTypesByName.TryGetValue(fullName, out var t)
        ? t
        : throw new ArgumentException($"Unknown error type '{fullName}'.");

    /// <summary>
    /// Retrieves the error type associated with the specified discriminator value.
    /// </summary>
    /// <remarks>This method looks up the discriminator in a predefined mapping of error types. Ensure that
    /// the discriminator is valid to avoid exceptions.</remarks>
    /// <param name="discriminator">The discriminator string used to identify the error type. Must not be null or empty.</param>
    /// <returns>The Type corresponding to the provided discriminator.</returns>
    /// <exception cref="ArgumentException">Thrown if the provided discriminator is not recognized.</exception>
    public static Type GetErrorTypeFromDiscriminator(String discriminator)
        => ErrorTypeByDiscriminator.TryGetValue(discriminator, out var t)
            ? t
            : throw new ArgumentException($"Unknown discriminator '{discriminator}'.", nameof(discriminator));

    /// <summary>
    /// Retrieves the discriminator value associated with the specified error type.
    /// </summary>
    /// <remarks>This method is used to obtain the discriminator for error handling and categorization. Ensure
    /// that the error type is registered before calling this method.</remarks>
    /// <param name="errorType">The type of the error for which to retrieve the discriminator. This type must be registered in the error
    /// registry.</param>
    /// <returns>The discriminator value corresponding to the specified error type.</returns>
    /// <exception cref="ArgumentException">Thrown if the specified error type is not registered in the error registry.</exception>
    public static String GetDiscriminator(Type errorType)
        => DiscriminatorByErrorType.TryGetValue(errorType, out var d)
            ? d
            : throw new ArgumentException($"Unregistered error type '{errorType.FullName}'.", nameof(errorType));

    /// <summary>
    /// Retrieves the <see cref="ErrorCode"/> instance associated with the specified <see cref="Error"/> ErrorType, creating
    /// and registering a new instance if one does not already exist.
    /// </summary>
    /// <typeparam name="TError"></typeparam>
    /// <returns>The singleton <see cref="ErrorCode"/> associated with the specified <see cref="Error"/> ErrorType.</returns>
    public static ErrorCode GetErrorCodeFor<TError>()
        where TError : Error => GetErrorCodeFor(typeof(TError));

    /// <summary>
    /// Retrieves the error code associated with the specified error type name.
    /// </summary>
    /// <remarks>This method searches all loaded assemblies for a type matching the provided name. Ensure that
    /// the error type is defined in one of the loaded assemblies.</remarks>
    /// <param name="errorTypeName">The name of the error type for which to retrieve the error code. This parameter must not be null or whitespace.</param>
    /// <returns>An instance of <see cref="ErrorCode"/> representing the error code corresponding to the specified error type.</returns>
    /// <exception cref="ArgumentException">Thrown if <paramref name="errorTypeName"/> is null, whitespace, or if no error type is found with the specified
    /// name.</exception>
    public static ErrorCode GetErrorCode(String errorTypeName)
    {
        if (String.IsNullOrWhiteSpace(errorTypeName))
        {
            throw new ArgumentException("Error type name must not be null or whitespace.", nameof(errorTypeName));
        }

        var errorType = AppDomain.CurrentDomain.GetAssemblies()
            .SelectMany(a => a.GetTypes())
            .FirstOrDefault(t => t.Name.Equals(errorTypeName, StringComparison.Ordinal))
            ?? throw new ArgumentException($"No Error type found with the name '{errorTypeName}'.", nameof(errorTypeName));

        return GetErrorCodeFor(errorType);
    }

    /// <summary>
    /// Retrieves the error code instance associated with the specified ErrorType, creating and registering a new instance if
    /// one does not already exist.
    /// </summary>
    /// <remarks>This method is thread-safe and ensures that only one error code instance is created per ErrorType.
    /// Use a valid, non-null ErrorType to avoid exceptions.</remarks>
    /// <param name="type">The ErrorType for which to retrieve the associated error code. This value must not be null.</param>
    /// <returns>An instance of <see cref="ErrorCode"/> associated with the specified ErrorType. If no instance exists, a new one is
    /// created and added to the registry.</returns>
    internal static ErrorCode GetErrorCodeFor(Type type)
        => LookupByType.GetOrAdd(type, CreateInstance);

    /// <summary>
    /// Removes all entries from the global lookup table, resetting it to an empty state.
    /// </summary>
    /// <remarks>This static method affects the global state of the lookup table. After calling this method,
    /// all previously stored entries are deleted, and subsequent lookups will return default values until new entries
    /// are added. Use caution when calling this method in multi-threaded scenarios, as it may impact other consumers
    /// relying on the lookup table's contents.</remarks>
    public static void Clear() => LookupByType.Clear();

    private static readonly Type ErrorType = typeof(Error);
    private static readonly Type ErrorCodeType = typeof(ErrorCode);
    private const String NotEmpty = "Not Empty";

    private static ErrorCode CreateInstance(Type errorType)
    {
        // Validate that the provided type is a subclass of Error
        if (!ErrorType.IsAssignableFrom(errorType))
        {
            throw new ArgumentException($"Type '{errorType.FullName}' must derive from '{ErrorType.FullName}'", nameof(errorType));
        }

        // Find the ErrorCode property
        var errorCodeProp = errorType.GetProperty(GeneratorConsts.ErrorCode, BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Instance)
            ?? throw new ArgumentException($"Type '{errorType.FullName}' must have a '{GeneratorConsts.ErrorCode}' property.", nameof(errorType));

        try
        {
            // Create an instance of the errorType
            var error = Activator.CreateInstance(errorType, NotEmpty);

            // Get the value of the Code property
            var codeValue = errorCodeProp.GetValue(error)
                ?? throw new ArgumentException($"Type '{errorType.FullName}' must have a non-null '{GeneratorConsts.ErrorCode}' property value.", nameof(errorType));

            // Get the type of the ErrorCode
            var errorCodeType = codeValue.GetType();
            if (!ErrorCodeType.IsAssignableFrom(errorCodeType))
            {
                throw new ArgumentException($"The '{GeneratorConsts.ErrorCode}' property of type '{errorType.FullName}' must be a derivative of {GeneratorConsts.Error}.", nameof(errorType));
            }

            // Get the Instance property
            var instanceProp = errorCodeType.GetField(GeneratorConsts.Instance, BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static);
            if (instanceProp is null)
            {
                // Fallback: Create a new instance. The error code type should be simple and have a parameterless
                // constructor, so this should be safe. Just means that there will be two instances of the error code.
                // The one created here and the one assigned to the ErrorCode.Instance property. Or maybe not, since
                // we were unable to retrieve it. That might mean that it is a custom error and we may not be able
                // deserialize it properly, but at least we can create an instance to return here.
                if (Activator.CreateInstance(errorCodeType) is ErrorCode fallback)
                {
                    return fallback;
                }

                throw new ArgumentException($"The type {errorType.FullName}.{errorCodeType.FullName} must have a static '{GeneratorConsts.Instance}' field with a not-null value, or a parameterless constructor.", nameof(errorType));
            }

            // Get the value of the Instance property
            if (instanceProp.GetValue(null) is not ErrorCode value)
            {
                throw new ArgumentException($"The static '{GeneratorConsts.Instance}' field of type '{errorCodeType.FullName}' must be of type '{ErrorCodeType.FullName}'.", nameof(errorType));
            }

            return value;
        }
        catch (MissingMethodException ex)
        {
            throw new ResultException($"Registering {errorType.FullName} failed because {ErrorCodeType.FullName} could not be created.", ex);
        }
    }
}
